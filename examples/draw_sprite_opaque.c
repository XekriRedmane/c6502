/* Draws an opaque sprite.
 *
 * width - the width of the sprite in bytes.
 * height - the height of the sprite (rows).
 * sprite_x - the column on the screen to draw at.
 * sprite_y - the row on the screen (0..191) to draw at.
 * tile_src - the data for the sprite.
 * page_flag - bit 7 indicates which page to draw on: 0 = page 1, 1 = page 2.
 */
__attribute__((zp_abi))
void draw_sprite_opaque(uint8_t width,
                        uint8_t height,
                        uint8_t sprite_x,
                        uint8_t sprite_y,
                        const uint8_t *tile_src,
                        uint8_t page_flag)
{
    const uint8_t *row_hi_tbl =
        (page_flag & 0x80) ? screen_row_addr_hi2 : screen_row_addr_hi;

    uint8_t y = 0;                                  
    for (uint8_t h = height; h != 0; h--, sprite_y++) {  
        uint8_t *row_base = (uint8_t *)(
            ((uint16_t)row_hi_tbl[sprite_y] << 8) |
            screen_row_addr_lo[sprite_y]);

        for (uint8_t row_remain = width, x = sprite_x;  
            (row_remain & 0x80) == 0;                  
            row_remain--, x--, y++) {                  
            if (x < 0x28) {                             
                row_base[x] = tile_src[y];
            }
        }
    }
}